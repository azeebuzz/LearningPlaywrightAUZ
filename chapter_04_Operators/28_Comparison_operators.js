// == Loose Equality Operator
// === Strict Equality Operator

// '5' == 5 // True
// '5' === 5 // False

// >, <, >=, <=, !=

console.log(3 > 4);
console.log(3 < 4);
console.log(4 >= 4);
console.log(3 <= 4);

console.log(5 == "5");
console.log(5 === "5");

console.log(5 != "5");
console.log(5 !== "5");